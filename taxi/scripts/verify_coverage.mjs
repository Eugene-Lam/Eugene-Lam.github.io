import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const iso = JSON.parse(fs.readFileSync(path.join(root, 'data/isochrones.json'), 'utf8'));

function inPoly(lng, lat, ring) {
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const [xi, yi] = ring[i], [xj, yj] = ring[j];
    if (((yi > lat) !== (yj > lat)) && (lng < (xj - xi) * (lat - yi) / (yj - yi) + xi))
      inside = !inside;
  }
  return inside;
}

function coveredBy(lat, lng) {
  const hits = [];
  for (const [id, f] of Object.entries(iso)) {
    const g = f.geometry;
    const polys = g.type === 'Polygon' ? [g.coordinates] : g.coordinates;
    if (polys.some(rings => inPoly(lng, lat, rings[0]))) hits.push(id);
  }
  return hits;
}

